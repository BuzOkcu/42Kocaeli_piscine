char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
#include <stdio.h>
int main()
{
char dest[] = "beyzaa";
char src[] = "soner";
ft_strcpy(dest,src);
printf("%s",dest);
}
